public class VariableDemo
{
         public static void main(String[] args) {
            boolean a=false;
            char c='a';
            byte b1=10;
            short s1=1000;
            int i=10;
            long l=1000;
            float f=10.0f;
            double d=10.03d;
            System.out.println("boolean value is\t"+a);
            System.out.println("char value is\t"+c);
            System.out.println("byte value is\t"+b1);
            System.out.println("short value is\t"+s1);
            System.out.println("int value is\t"+i);
            System.out.println("long value is\t"+l);
            System.out.println("float value is\t"+f);
            System.out.println("double value is\t"+d);
        }
    }